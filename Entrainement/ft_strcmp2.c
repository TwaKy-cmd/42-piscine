int ft_strcmp(char *s1, char *s2)
{
    int i;
    int rep;
    
    i = 0;
    rep = 0;
    while (s1[i] || s2[i])
    {
        rep = s1[i] - s2[i];
        if (rep == 0)
            i++;
        else
            return (rep);
    }
    return (rep);
}